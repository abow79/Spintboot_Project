package com.example.api.demo;

public class People {
    String id;
    String name;
    String gender;

    public String getId() {
        return id;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String Gender) {
        this.gender = Gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String Name) {
        this.name = Name;
    }

    public void setId(String id) {
        this.id = id;
    }

}
